function createMentionStatus(
  id: string,
  account: import('@repo/types').Account,
  content: string,
  tags: string[],
  createdAt: string,
  stats: { replies: number; reblogs: number; favourites: number },
): Status {
  const domain = account.acct.split('@')[1] || 'social.network';
  return {
    id,
    uri: `https://${domain}/statuses/${id}`,
    createdAt,
    editedAt: null,
    account,
    content: `<p>${content}</p>`,
    visibility: 'public',
    sensitive: false,
    spoilerText: '',
    mediaAttachments: [],
    application: { name: 'Web' },
    mentions: [],
    tags: tags.map(t => ({ id: t, name: t, url: `/tags/${t}` })),
    emojis: [],
    reblogsCount: stats.reblogs,
    favouritesCount: stats.favourites,
    repliesCount: stats.replies,
    quotesCount: 0,
    quoteApproval: {
      automatic: ['public'],
      manual: [],
      currentUser: 'automatic',
    },
    url: `https://${domain}/@${account.username}/${id}`,
    inReplyToId: null,
    inReplyToAccountId: null,
    reblog: null,
    poll: null,
    card: null,
    language: 'en',
    text: null,
    favourited: false,
    reblogged: false,
    muted: false,
    bookmarked: false,
    pinned: false,
  };
}

